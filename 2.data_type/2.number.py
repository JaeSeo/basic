# 정수형 (Integer)
# 파이썬의 규칙상, 정수형과 정수형 간의 연산 결과는 정수형으로 나옵니다.

# 그러나 '나눗셈' 연산에는 예외가 적용됩니다. 나눗셈의 경우에는, 정수형과 정수형 간의 연산이더라도 결과값이 항상 소수형(floating point)입니다. 예를 들어 print(8 / 2)의 결과값은 소수형 4.0인 거죠.

# 결과값이 정수형으로 나옴
print(1 + 2)        # 1 더하기 2
print(6 - 2)        # 6 빼기 2
print(3 * 4)        # 3 곱하기 4
print(11 % 4)       # 11을 4로 나눈 나머지
print(3 ** 2)       # 3의 2제곱

# 나눗셈 예외: 결과값이 소수형으로 나옴
print(7 / 2)        # 7 나누기 2
print(8 / 2)        # 8 나누기 2
