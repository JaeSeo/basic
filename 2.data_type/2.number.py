# 소수형 (Floating Point)
# 정수형과 소수형 간의 연산, 또는 소수형끼리의 연산 결과는 항상 소수형입니다.

print(1.5 + 2.5)
print(1.5 * (2 + 1))
print(3.0 / 2)
print(6 / 3)      # 정수형끼리의 연산이라도 나눗셈 결과는 소수형
