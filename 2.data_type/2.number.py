# 연산 부호 (Operators)
# 연산자	뜻
# +	덧셈 (addition)
# -	뺄셈 (subtraction)
# *	곱셈 (multiplication)
# /	나눗셈 (division)
# %	나머지 (modulo)
# **	거듭제곱 (exponentiation)
print(4 + 7)          # 4 더하기 7
print(2 - 4)          # 2 빼기 4
print(5 * 3)          # 5 곱하기 3
print(7 % 3)          # 7을 3으로 나눈 나머지
print(2 ** 3)         # 2의 3제곱

print(2 + 3 * 2)      # 덧셈보다 곱셈을 먼저 계산
print(2 * (2 + 3))    # 괄호안의 수식을 먼저 계산
